package com.rdcentermrzhi.java.demo.kryo;

public class Student {

		private int a;
		private int b;
		private int c;
		private String d;
		private long g;
		private float f;
		public int getA() {
			return a;
		}
		public void setA(int a) {
			this.a = a;
		}
		public int getB() {
			return b;
		}
		public void setB(int b) {
			this.b = b;
		}
		public int getC() {
			return c;
		}
		public void setC(int c) {
			this.c = c;
		}
		public String getD() {
			return d;
		}
		public void setD(String d) {
			this.d = d;
		}
		public long getG() {
			return g;
		}
		public void setG(long g) {
			this.g = g;
		}
		public float getF() {
			return f;
		}
		public void setF(float f) {
			this.f = f;
		}
		public Student(int a, int b, int c, String d, long g, float f) {
			super();
			this.a = a;
			this.b = b;
			this.c = c;
			this.d = d;
			this.g = g;
			this.f = f;
		}
		
		
}
